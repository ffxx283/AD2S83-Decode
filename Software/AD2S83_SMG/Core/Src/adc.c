float ADC_Get_Voltage(uint8_t Channels)
{
	float Vol_Num = 0;
	uint32_t ADC_Num = 0;

	switch (Channels) {
		case 1:
			HAL_ADC_Start( &hadc1 );
			HAL_ADC_PollForConversion( &hadc1 , 10);
			ADC_Num = HAL_ADC_GetValue( &hadc1 );
			Vol_Num = 3.3*ADC_Num/4096*2;
			break;
		case 2:
			HAL_ADC_Start( &hadc1 );
			HAL_ADC_PollForConversion( &hadc1 , 10);
			ADC_Num = HAL_ADC_GetValue( &hadc1 );
			Vol_Num = 3.3*ADC_Num/4096*11;
			break;
		case 3:
			HAL_ADC_Start( &hadc1 );
			HAL_ADC_PollForConversion( &hadc1 , 10);
			ADC_Num = HAL_ADC_GetValue( &hadc1 );
			Vol_Num = 3.3*ADC_Num/4096*2;
			break;
		default:
			Vol_Num = 0;
			break;
	}

	return Vol_Num;
}

uint8_t POWER_IS_GOOD(void)
{
	uint8_t reasult = 0;
	float POWER = 0;

	POWER = ADC_Get_Voltage(1);
	if( (POWER<=5*0.9)||(POWER>5*1.1) )
		reasult = 1;
	POWER = ADC_Get_Voltage(2);
	if( (POWER<=12*0.9)||(POWER>12*1.1) )
		reasult = 2;
	POWER = ADC_Get_Voltage(3);
	if( (POWER<=5*0.9)||(POWER>5*1.1) )
		reasult = 3;

	return reasult;

}







